public class Begin10 {

    public static void main(String[] args) {

        double a = 2, b = 4, x, y, z, j;

        x = Math.pow(a, 2) + Math.pow(b, 2);
        y = Math.pow(a, 2) - Math.pow(b, 2);
        z = Math.pow(a, 2) * Math.pow(b, 2);
        j = Math.pow(a, 2) / Math.pow(b, 2);

        System.out.println("a+b=" + x + " a-b=" + y + " a*b=" + z + " a/b=" + j);

    }
}
